public class Estudiante {
    String nombre;
    int edad ;
    float calificaiones;
    Estudiante (String nombre, int edad, float calificaiones){
    }

    public String getNombre() {
        return nombre;
    }

    public int getEdad() {
        return edad;
    }

    public float getCalificaiones() {
        return calificaiones;
    }
    public String toString() {
        return super.toString();
    }
}
